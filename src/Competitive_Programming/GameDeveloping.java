package Competitive_Programming;

import java.util.Scanner;

public class GameDeveloping {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int l[] = new int[n];
        int v[] = new int[n];
        int r[] = new int[n];
        for (int i = 0; i < n; i++){
            l[i] = sc.nextInt();
            v[i] = sc.nextInt();
            r[i] = sc.nextInt();
        }

    }
}
